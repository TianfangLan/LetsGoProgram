package jshell.jshellcore;

/***
 * the class will store the input message
 * 
 * @author Jiahong Wang
 *
 */
public class Input {

  private String inputContent;
  private int line;
 
  public Input(int line, String content) {
    this.line = line;
    this.inputContent = content;
  }
  /**
   * get the line number
   * @return an int of the line number
   */
  public int getLine() {
    return line;
  }
  
  /**
   * get the input as a String
   * @return the input
   */
  public String GetInputContent() {
    return this.inputContent;
  }
  
  @Override
  public String toString() {
    return line+": "+inputContent;
  }
}
